class MyHomePage extends StatefulWidget {
  const MyHomePage({Key? key, required this.title}) : super(key: key);

  final String title;

  @override
  _MyHomePageState createState() => _MyHomePageState();
}

class _MyHomePageState extends State<MyHomePage> {
  @override
  void initState() {
    super.initState();
  }

  updateRemoteConfig() async {
    await CustomFirebaseRemoteConfig().forceFetch();

    setState(() {});
  }

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      appBar: AppBar(
        title: Text(widget.title),
      ),
      body: Container(
        color: CustomFirebaseRemoteConfig()
                .getValueOrDefault(key: 'testing', defaultValue: false)
            ? Colors.black
            : Colors.blue,
        child: const Center(
          child: Text('Testing feature flag'),
        ),
      ),
      bottomNavigationBar: ElevatedButton(
        onPressed: updateRemoteConfig,
        child: const Text('teste'),
      ),
    );
  }
}
